public class oneorder
{
    private int row1;
    private int col1;
    private int num1;
    public oneorder(int a, int b, int c)
    {
        row1 = a;
        col1 = b;
        num1 = c;
    }
    public int getrow()
    {
        return row1;
    }
    public int getcol()
    {
        return col1;
    }
    public int getnum()
    {
        return num1;
    }
    public void setnum(int d)
    {
        num1 = d;
    }
}
