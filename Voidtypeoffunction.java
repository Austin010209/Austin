public class Voidtypeoffunction
{
    public static void Austin()
    {
        System.out.println("hi");
    }
    public static void Eric()
    {
        System.out.println("My name is Tom");
    }
    public static void William()
    {
        Austin();
        Eric();
    }
    public static void main(String[] args)
    {
        William();
        System.out.println("I am from MOT");
    }
}